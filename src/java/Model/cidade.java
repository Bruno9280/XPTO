package Model;

/**
 *
 * @author bruno
 */
public class cidade {

    private String name;

    public cidade() {
    }

    public cidade(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "capitais{" + "name=" + name + '}';
    }

}
